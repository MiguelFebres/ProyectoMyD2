    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        // TODO add your handling code here:
        String usern = this.username1.getText().toLowerCase();
        String prior = this.priority.getText();
        boolean verificado = false;
        if(prior.equals("1")){
            prior = "prioridad_alta";
            verificado = true;
        }else if (prior.equals("2")){
            prior = "prioridad_media";
            verificado = true;
        }else if (prior.equals("3")){
            prior = "prioridad_baja";
            verificado = true;}
        if(verificado){
            if(tabla_usuarios.buscar_usuario(usern) != null && !tabla_usuarios.buscar_usuario(usern).getNombre_usuario().equals("")){
                JOptionPane.showMessageDialog(rootPane, "El usuario ya se encuentra registrado", "Error", HEIGHT);
            }else{
                tabla_usuarios.registrar_usuario(usern, prior);
                JOptionPane.showMessageDialog(rootPane, "Usuario Registrado Exitosamente", "Crear Usuario", HEIGHT);
            }
            this.reg.setText(tabla_usuarios.usuarios_textArea(""));}
        else{
            JOptionPane.showMessageDialog(rootPane, "INTRODUZCA UNA PRIORIDAD V√ÅLIDA");
        }
    }                                        

